$(document).ready(function(){
    $('#resetPasswordForm').hide();
    $('#registerForm').hide();
    $('#reset').click(function(){
        $('#loginForm').hide();
        $('#registerForm').hide();
        $(".status").hide();
        $('#resetPasswordForm').fadeIn("slow");
    });
    $('#register').click(function(){
        $('#loginForm').hide();
        $('#resetPasswordForm').hide();
        $(".status").hide();
        $('#registerForm').fadeIn("slow");
    });
    $('#login').click(function(){
        $('#registerForm').hide();
        $('#resetPasswordForm').hide();
        $(".status").hide();
        $('#loginForm').fadeIn("slow");
    });
})